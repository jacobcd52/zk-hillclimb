"""R_rank covert-channel capacity (rank entropy) vs MODEL SCALE across families.

For each model we reproduce the CORRECTED codebook capacity orientation
(reference = codebook M_int post-Gumbel scores, served = FP8 post-Gumbel argmax,
shared Gumbel seed), but on ON-POLICY tokens generated by the model itself
(T=1.0, top_p=1.0). We score ONLY completion positions, compute the
post-Gumbel rank of the served token at each completion position, and report

    R_rank = Shannon entropy (bits) of the empirical rank distribution
           = -sum_r p_r log2 p_r

over all kept completion tokens, where p_r = fraction of completion tokens whose
served-token rank == r. Vocab-agnostic (read from logits.shape[-1]).

KEY FIX (vs prior run): INSTRUCT / chat models are now generated WITH their chat
template (apply_chat_template, add_generation_prompt=True). BASE models keep plain
prompt continuation. The prior run fed bare instructions to FP8 *instruct*
checkpoints -> off-distribution degenerate generation -> extreme activations ->
the codebook per-token FP8 int path loses precision on outlier rows -> huge logit
errors -> inflated R_rank/mean_rank. We test whether the template fix resolves it.

DIAGNOSTICS recorded per model (to decide artifact vs real):
  - mean_rank, max_rank (served-token post-Gumbel rank stats)
  - sat_rate      : fraction of completion positions where the max |z_fp8 - z_cb|
                    logit divergence exceeds SAT_LOGIT_THRESH (proxy for the
                    activation-outlier saturation that wrecks the codebook int path)
  - act_absmax_p99/max : 99th-pct / max per-token activation absmax seen by the
                    swapped linears during the codebook forward (hooks). Large
                    values => outlier rows => big per-token-FP8 quantization error.

Pipeline (per model, ONE at a time, bf16):
  1. generate on-policy completions (chat template if instruct), >=~5000 comp tokens
  2. FP8 teacher (served) teacher-forced forward over full sequences
  3. codebook M_int (reference) teacher-forced forward, WITH activation hooks
  4. post-Gumbel rank at each pos; keep completion-position ranks only
  5. R_rank = entropy of rank distribution; diagnostics as above

Run:
  IMA_TEACHER_KERNEL=fp8_scaled_mm /root/int-model-env/bin/python \
      capacity/rank_entropy_sweep.py --nprompts 64
Validate one model first:
  ... capacity/rank_entropy_sweep.py --only qwen2.5-0.5b-fp8
"""
